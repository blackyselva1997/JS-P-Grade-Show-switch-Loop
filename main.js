let mark = String(prompt("Enter a Grade"));

switch (mark){
    case 'A':
        document.write("Outstanding");
        break;
    case 'B':
        document.write("Excellent");
        break;
    case 'C':
        document.write("Well done");
        break;
    case 'D':
        document.write("Passed");
        break;
    case 'F':
        document.write("Failed");
        break;
        default:
}